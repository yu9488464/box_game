PUBLIC GOAL
EXTRN GOCXL:WORD,GODXL:WORD
	.MODEL SMALL 
	.STACK
	.DATA
	GOCXR DW ?
	GODXR DW ?
	GOCXU DW ?
	GODXU DW ?
	GOCXD DW ?
	GODXD DW ?
	.CODE
GOAL PROC 
;======================目标标记部分变量计算赋值======================
	MOV CX,GOCXL
	ADD CX,4
	MOV GOCXU,CX
	MOV GOCXD,CX
	ADD CX,4
	MOV GOCXR,CX
	
	MOV DX,GODXL
	MOV GODXR,DX
	ADD DX,4
	MOV GODXD,DX
	SUB DX,8
	MOV GODXU,DX
;=========================划线=================================	
	
	MOV CX,GOCXL
	MOV DX,GODXL
L_U:
	MOV AH,0CH
	MOV AL,0FH
	INT 10H
	INC CX
	DEC DX
	CMP CX,GOCXU
	JC L_U
	
	MOV CX,GOCXU
	MOV DX,GODXU
U_R:
	MOV AH,0CH
	MOV AL,0FH
	INT 10H
	INC CX
	INC DX
	CMP CX,GOCXR
	JC U_R
	
	MOV CX,GOCXR
	MOV DX,GODXR
R_D:
	MOV AH,0CH
	MOV AL,0FH
	INT 10H
	DEC CX
	INC DX
	CMP DX,GODXD
	JC R_D
	
	MOV CX,GOCXL
	MOV DX,GODXL
L_D:
	MOV AH,0CH
	MOV AL,0FH
	INT 10H
	INC CX
	INC DX
	CMP CX,GOCXD
	JC L_D
	
	RET
GOAL ENDP
END




